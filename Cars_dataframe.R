car_model<-c("Honda","Toyota","Kia","BMW","Merc","Maruthi","Hyundai")
engine_cc<-c(1600,1800,1346,1020,2500,1200,1400)
milage<-c(30,15,10,7,5,20,18)
price<-c(15,18,14,22,25,8,16)
cars_data<-data.frame(car_model,engine_cc,milage,price)
print(cars_data)
relation1<-lm(cars_data$price~cars_data$engine_cc) 
# remember here we use ~
relation2<-lm(cars_data$milage~cars_data$engine_cc)
c1<-cov(price,engine_cc)
cr1<-cor(price,engine_cc)
c2<-cov(milage,engine_cc)
cr2<-cor(milage,engine_cc)
print(relation1)
print(relation2)
print(c1)
print(cr1)
print(c2)
print(cr2)

